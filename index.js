module.exports = {

    /**
     * Suma de dos numeros
     * @param {*} n1 
     * @param {*} n2 
     * @returns 
     */
    suma: function (n1, n2) {
        return n1 + n2;
    },

    /**
     * Resta de dos numeros
     * @param {*} n1 
     * @param {*} n2 
     * @returns 
     */
    resta: function (n1, n2) {
        return n1 - n2;
    },

    /**
     * multiplicacion de dos numeros
     * @param {*} n1 
     * @param {*} n2 
     * @returns 
     */
    multiplicacion: function (n1, n2) {
        return n1 * n2;
    },

    /**
     * division de dos numeros
     * @param {*} n1 
     * @param {*} n2 
     * @returns 
     */
    division: function (n1, n2) {
        return n1 / n2;
    },
}